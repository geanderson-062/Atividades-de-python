# Definindo os números na vareavel
numero1 = 5
numero2 = 5

# Realizando as operações básicas

# numero 1 soma com numero 2 e guarda a operação na vareavel adição
adicao = numero1 + numero2

# numero 1 subtrai com numero 2 e guarda a operação na vareavel subtração
subtracao = numero1 - numero2

# numero 1 multiplica com numero 2 e guarda a operação na vareavel multiplicação
multiplicacao = numero1 * numero2

# numero 1 divide com numero 2 e guarda a operação na vareavel divisão
divisao = numero1 / numero2

# Exibindo os resultados da quatro operações
print("Resultado da adição:", adicao)
print("Resultado da subtração:", subtracao)
print("Resultado da multiplicação:", multiplicacao)
print("Resultado da divisão:", divisao)
